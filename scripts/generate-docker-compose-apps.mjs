#!/usr/bin/env node
/**
 * Writes docker-compose.apps.yml — static catalog apps (simple-deploy-repos) + Node runtimes
 * (escape_among_us) + Python runtimes (e.g. Gradio workshop). Others stay stub-only until extended.
 * dwani-ai-uber-app reuses the hub image (same Dockerfile as service `hub`) on a second hostname.
 */
import { writeFileSync, readFileSync, existsSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";
import { applyEnv } from "./lib/load-env.mjs";
import {
  isSimpleDeployRepo,
  catalogAppDir,
  catalogAppBuildCmd,
  catalogAppArtifactDir,
} from "./lib/simple-deploy-repos.mjs";
import {
  isRuntimeNodeRepo,
  runtimeNodePort,
} from "./lib/runtime-node-repos.mjs";
import {
  isRuntimePythonRepo,
  runtimePythonBuild,
} from "./lib/runtime-python-repos.mjs";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
applyEnv(root);

const manifestPath = join(root, "web/public/projects.v1.json");
if (!existsSync(manifestPath)) {
  console.error("generate-docker-compose-apps: missing", manifestPath);
  process.exit(1);
}

const manifest = JSON.parse(readFileSync(manifestPath, "utf8"));
const projects = manifest.projects;
if (!Array.isArray(projects)) {
  console.error("Invalid manifest");
  process.exit(1);
}

const lines = [
  "# Generated by scripts/generate-docker-compose-apps.mjs — do not edit by hand.",
  "# Regenerate: npm run compose:apps (included in deploy after manifest).",
  "# catalog-app = static nginx; escape-among-us = Next :3000; workshop = Gradio :8000;",
  "# agent-olympics-school = Gradio :8080 + FastAPI sidecar :8000;",
  "# dwani-api-server :18888, faster-whisper-server :8000 (manifest liveUrl → …/docs for Swagger in hub iframe).",
  "",
  "services:",
];

let count = 0;
for (const p of projects) {
  const id = p.id;
  const repo = p.repo;
  if (!id || !repo) continue;

  // Same static hub as service `hub`; extra hostname matches catalog id / liveUrl consistency.
  if (id === "dwani-ai-uber-app" && isSimpleDeployRepo(repo)) {
    lines.push(`  dwani-ai-uber-app:`);
    lines.push(`    build: .`);
    lines.push(`    image: uber-app-hub:local`);
    lines.push(`    restart: unless-stopped`);
    lines.push(`    networks:`);
    lines.push(`      - edge`);
    lines.push(`    labels:`);
    lines.push(`      - traefik.enable=true`);
    lines.push(`      - traefik.docker.network=edge`);
    lines.push(
      `      - traefik.http.routers.dwani-ai-uber-app.rule=Host(\`dwani-ai-uber-app.\${DOMAIN:-localhost}\`)`
    );
    lines.push(`      - traefik.http.routers.dwani-ai-uber-app.entrypoints=web`);
    lines.push(
      `      - traefik.http.services.dwani-ai-uber-app.loadbalancer.server.port=80`
    );
    lines.push("");
    count++;
    continue;
  }

  if (isRuntimeNodeRepo(repo)) {
    const port = runtimeNodePort(repo);
    lines.push(`  ${id}:`);
    lines.push(`    build:`);
    lines.push(`      context: ./runtime/escape-among-us`);
    lines.push(`      dockerfile: Dockerfile`);
    lines.push(`    image: uber-app-${id}:local`);
    lines.push(`    restart: unless-stopped`);
    lines.push(`    networks:`);
    lines.push(`      - edge`);
    lines.push(`    labels:`);
    lines.push(`      - traefik.enable=true`);
    lines.push(`      - traefik.docker.network=edge`);
    lines.push(
      `      - traefik.http.routers.${id}.rule=Host(\`${id}.\${DOMAIN:-localhost}\`)`
    );
    lines.push(`      - traefik.http.routers.${id}.entrypoints=web`);
    lines.push(
      `      - traefik.http.services.${id}.loadbalancer.server.port=${port}`
    );
    lines.push("");
    count++;
    continue;
  }

  if (isRuntimePythonRepo(repo)) {
    const cfg = runtimePythonBuild(repo);
    if (!cfg) continue;
    const port = cfg.port;
    const mainDockerfile = cfg.dockerfile || "Dockerfile";

    if (cfg.apiSidecar) {
      const sc = cfg.apiSidecar;
      lines.push(`  ${sc.serviceId}:`);
      lines.push(`    build:`);
      lines.push(`      context: ${cfg.context}`);
      lines.push(`      dockerfile: ${sc.dockerfile}`);
      lines.push(`    image: uber-app-${sc.serviceId}:local`);
      lines.push(`    restart: unless-stopped`);
      lines.push(`    environment:`);
      if (sc.composeEnv) {
        for (const [k, v] of Object.entries(sc.composeEnv)) {
          lines.push(`      ${k}: ${v}`);
        }
      }
      lines.push(`    networks:`);
      lines.push(`      - edge`);
      lines.push("");
      count++;
    }

    lines.push(`  ${id}:`);
    lines.push(`    build:`);
    lines.push(`      context: ${cfg.context}`);
    lines.push(`      dockerfile: ${mainDockerfile}`);
    lines.push(`    image: uber-app-${id}:local`);
    lines.push(`    restart: unless-stopped`);
    if (cfg.dependsOn?.length) {
      lines.push(`    depends_on:`);
      for (const dep of cfg.dependsOn) {
        lines.push(`      - ${dep}`);
      }
    }
    lines.push(`    environment:`);
    lines.push(`      DWANI_API_KEY: \${DWANI_API_KEY:-}`);
    lines.push(`      DWANI_API_BASE_URL: \${DWANI_API_BASE_URL:-}`);
    if (cfg.apiSidecar) {
      const internalUrl = `http://${cfg.apiSidecar.serviceId}:${cfg.apiSidecar.port}/`;
      const skipSchoolUrl =
        cfg.composeEnv && Object.hasOwn(cfg.composeEnv, "SCHOOL_BACKEND_URL");
      if (!skipSchoolUrl) {
        lines.push(`      SCHOOL_BACKEND_URL: ${internalUrl}`);
      }
    }
    if (cfg.composeEnv) {
      for (const [k, v] of Object.entries(cfg.composeEnv)) {
        lines.push(`      ${k}: ${v}`);
      }
    }
    lines.push(`    networks:`);
    lines.push(`      - edge`);
    lines.push(`    labels:`);
    lines.push(`      - traefik.enable=true`);
    lines.push(`      - traefik.docker.network=edge`);
    lines.push(
      `      - traefik.http.routers.${id}.rule=Host(\`${id}.\${DOMAIN:-localhost}\`)`
    );
    lines.push(`      - traefik.http.routers.${id}.entrypoints=web`);
    lines.push(
      `      - traefik.http.services.${id}.loadbalancer.server.port=${port}`
    );
    lines.push("");
    count++;
    continue;
  }

  if (!isSimpleDeployRepo(repo)) {
    continue;
  }

  const repoQuoted = JSON.stringify(repo);

  lines.push(`  ${id}:`);
  lines.push(`    build:`);
  lines.push(`      context: ./runtime/catalog-app`);
  lines.push(`      dockerfile: Dockerfile`);
  lines.push(`      args:`);
  lines.push(`        REPO: ${repoQuoted}`);
  const appDir = catalogAppDir(repo);
  if (appDir) {
    lines.push(`        CATALOG_APP_DIR: ${JSON.stringify(appDir)}`);
  }
  const buildCmd = catalogAppBuildCmd(repo);
  if (buildCmd) {
    lines.push(`        CATALOG_APP_BUILD_CMD: ${JSON.stringify(buildCmd)}`);
  }
  const artifactDir = catalogAppArtifactDir(repo);
  if (artifactDir) {
    lines.push(`        CATALOG_APP_ARTIFACT_DIR: ${JSON.stringify(artifactDir)}`);
  }
  lines.push(`    restart: unless-stopped`);
  lines.push(`    networks:`);
  lines.push(`      - edge`);
  lines.push(`    labels:`);
  lines.push(`      - traefik.enable=true`);
  lines.push(`      - traefik.docker.network=edge`);
  lines.push(
    `      - traefik.http.routers.${id}.rule=Host(\`${id}.\${DOMAIN:-localhost}\`)`
  );
  lines.push(`      - traefik.http.routers.${id}.entrypoints=web`);
  lines.push(
    `      - traefik.http.services.${id}.loadbalancer.server.port=${80}`
  );
  lines.push("");
  count++;
}

const outFile = join(root, "docker-compose.apps.yml");
writeFileSync(outFile, lines.join("\n") + "\n", "utf8");
console.log(
  "Wrote",
  outFile,
  "—",
  count,
  "compose service(s); other catalog ids use Traefik stubs."
);
