#!/usr/bin/env node
/**
 * Writes traefik/dynamic/projects-stubs.yml — one Host router per catalog id → placeholder.
 * Skips ids listed in STUB_EXCLUDE (comma-separated) and services named in docker-compose.apps.yml.
 */
import {
  readFileSync,
  writeFileSync,
  mkdirSync,
  existsSync,
  unlinkSync,
} from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";
import { applyEnv } from "./lib/load-env.mjs";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
applyEnv(root);

const domain = (
  process.env.DOMAIN ||
  process.env.UBERAPP_DEPLOY_DOMAIN ||
  "localhost"
).trim();

const manifestPath = join(root, "web/public/projects.v1.json");
if (!existsSync(manifestPath)) {
  console.error("Missing", manifestPath);
  process.exit(1);
}

/** @param {string} composePath @returns {Set<string>} */
function loadComposeTopLevelServices(composePath) {
  const set = new Set();
  if (!existsSync(composePath)) return set;
  const src = readFileSync(composePath, "utf8");
  let inServices = false;
  for (const line of src.split("\n")) {
    const t = line.replace(/\s+$/, "");
    if (/^services:\s*$/.test(t)) {
      inServices = true;
      continue;
    }
    if (inServices && /^(networks|volumes|configs|secrets):\s*$/.test(t)) break;
    if (!inServices) continue;
    const m = t.match(/^ {2}([a-zA-Z0-9_.-]+):\s*$/);
    if (m) set.add(m[1]);
  }
  return set;
}

const manifest = JSON.parse(readFileSync(manifestPath, "utf8"));
const projects = manifest.projects;
if (!Array.isArray(projects)) {
  console.error("Invalid manifest: projects is not an array");
  process.exit(1);
}

const exclude = new Set(
  (process.env.STUB_EXCLUDE || "")
    .split(",")
    .map((s) => s.trim())
    .filter(Boolean)
);
for (const s of loadComposeTopLevelServices(join(root, "docker-compose.apps.yml"))) {
  exclude.add(s);
}

const lines = [
  "# Generated by scripts/generate-traefik-stubs.mjs — do not edit by hand",
  "http:",
  "  routers:",
];

let count = 0;
for (const p of projects) {
  const { id } = p;
  if (!id || exclude.has(id)) continue;
  const host = `${id}.${domain}`;
  const routerName = `stub-${id.replace(/[^a-zA-Z0-9_-]/g, "-")}`;
  lines.push(`    ${routerName}:`);
  lines.push(`      rule: Host(\`${host}\`)`);
  lines.push("      service: project-stub");
  lines.push("      entryPoints:");
  lines.push("        - web");
  count++;
}

if (count === 0) {
  const outDir = join(root, "traefik/dynamic");
  const outFile = join(outDir, "projects-stubs.yml");
  if (existsSync(outFile)) unlinkSync(outFile);
  console.log(
    "No stub routers; removed projects-stubs.yml if present — all catalog hosts use compose services."
  );
  process.exit(0);
}

lines.push("  services:");
lines.push("    project-stub:");
lines.push("      loadBalancer:");
lines.push("        servers:");
lines.push('          - url: "http://placeholder:80"');

const outDir = join(root, "traefik/dynamic");
mkdirSync(outDir, { recursive: true });
const outFile = join(outDir, "projects-stubs.yml");
writeFileSync(outFile, lines.join("\n") + "\n", "utf8");
console.log(
  "Wrote",
  outFile,
  "—",
  count,
  "stub router(s);",
  exclude.size,
  "id(s) excluded (apps env + STUB_EXCLUDE)"
);
